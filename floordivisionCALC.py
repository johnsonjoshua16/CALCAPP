# Now This is for floor division---------------------------------------------------------------
def floor_division_numbers(num1, num2):
    return num1 // num2

# Input from the user
number1 = float(input("Enter the first number: "))
number2 = float(input("Enter the second number: "))

# Calculate the floor division
result = floor_division_numbers(number1, number2)

# Display the result
print(f"The floor division of {number1} and {number2} is {result}.")

